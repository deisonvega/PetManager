
package Classes;

public class clsReporteDeRaza {
    
    private String raza;
    private int contarraza;

    public clsReporteDeRaza() {
        this.raza = "";
        this.contarraza =0;
    }
    

    /**
     * @return the raza
     */
    public String getRaza() {
        return raza;
    }

    /**
     * @param raza the raza to set
     */
    public void setRaza(String raza) {
        this.raza = raza;
    }

    /**
     * @return the contarraza
     */
    public int getContarraza() {
        return contarraza;
    }

    /**
     * @param contarraza the contarraza to set
     */
    public void setContarraza(int contarraza) {
        this.contarraza = contarraza;
    }

    
    
    
    
}
